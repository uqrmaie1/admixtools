# ---------------------------------------------------------------------------
# T1.8 — Performance bound. read_legofit_output on a 100-parameter
# synthetic .legofit completes in under 1 second.
# ---------------------------------------------------------------------------

test_that("T1.8: read_legofit_output handles a 100-parameter .legofit in under 1s", {
  skip_on_cran()
  # Synthesize a large .legofit with 50 fixed + 50 free params
  n <- 50
  free_lines  <- sprintf("     T_n%03d = %g", seq_len(n), runif(n, 0.1, 2))
  fixed_lines <- sprintf("       T_f%03d = 0", seq_len(n))
  one_line    <- "       one = 1"
  content <- c(
    "Initial parameter values",
    "Fixed:", fixed_lines, one_line,
    "Free:", free_lines,
    "DiffEv reached_goal. cost=1e-10 spread=1e-7",
    "Fitted parameter values",
    "Free:", free_lines
  )
  tmp <- tempfile(fileext = ".legofit")
  on.exit(unlink(tmp))
  writeLines(content, tmp)
  t <- system.time(suppressMessages(
    read_legofit_output(tmp, graph = NULL)
  ))[["elapsed"]]
  expect_lt(t, 1.0)
})

test_that("T1.8: parse_bootci_output handles a 100-row .bootci in under 1s", {
  skip_on_cran()
  n <- 100
  header <- "       par             est             low            high"
  data_rows <- sprintf("    T_n%03d   %.8f   %.8f   %.8f",
                       seq_len(n), runif(n), runif(n, 0, 0.5), runif(n, 0.5, 1))
  content <- c(
    "# bootci.py run at: 2026-05-21",
    "# confidence: 0.950",
    header, data_rows
  )
  t <- system.time(parse_bootci_output(content))[["elapsed"]]
  expect_lt(t, 1.0)
})

