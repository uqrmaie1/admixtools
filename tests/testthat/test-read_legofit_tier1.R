# ---------------------------------------------------------------------------
# T1.7 — finished_iterations convergence inform fires on a real
# underconverged fit (generated by capping legofit at -S 1 generation).
# ---------------------------------------------------------------------------

test_that("T1.7: legofit_fit_incomplete fires on real underconverged .legofit (graph= supplied)", {
  expect_message(
    read_legofit_output(test_path("fixtures", "ourex1-underconverged.legofit"),
                        graph = make_ourex1_graph()),
    class = "legofit_fit_incomplete"
  )
})

test_that("T1.7: legofit_fit_incomplete fires on real underconverged .legofit (graph=NULL)", {
  # Use a fresh frequency_id by deduping on path: T1.7-graph and T1.7-null share
  # the same fixture path, so the .frequency_id is the same — the second call
  # would be deduped. Use a different copy to give it a unique path.
  tmp <- tempfile(fileext = ".legofit")
  on.exit(unlink(tmp))
  file.copy(test_path("fixtures", "ourex1-underconverged.legofit"), tmp)
  expect_message(
    read_legofit_output(tmp, graph = NULL),
    class = "legofit_fit_incomplete"
  )
})

test_that("T1.7: fit_convergence attribute carries finished_iterations status", {
  result <- suppressMessages(read_legofit_output(
    test_path("fixtures", "ourex1-underconverged.legofit"),
    graph = make_ourex1_graph()
  ))
  conv <- attr(result, "fit_convergence")
  expect_equal(conv$status, "finished_iterations")
  expect_gt(conv$spread, 1e-4)   # spread is large because underconverged
})

