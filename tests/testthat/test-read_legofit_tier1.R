# T1.4 — include_fixed = FALSE parity across coalescent-unit and scalar-twoN
# topologies. The free-fitted parameter set must be identical in both modes.
# ---------------------------------------------------------------------------

test_that("T1.4: include_fixed parity for coalescent-unit ourex1", {
  full     <- read_legofit_output(test_path("fixtures", "ourex1.legofit"),
                                  graph = NULL)
  no_fixed <- read_legofit_output(test_path("fixtures", "ourex1.legofit"),
                                  graph = NULL, include_fixed = FALSE)
