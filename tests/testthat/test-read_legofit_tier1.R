# ---------------------------------------------------------------------------
# T1.5 — bootci.py output variants. Synthesize bootci files with optional
# `lbl` column and varying `--confidence` headers.
# ---------------------------------------------------------------------------

test_that("T1.5: bootci with -l label produces lbl column", {
  lbl_content <- c(
    "# bootci.py run at: 2026-05-21 10:00:00",
    "# input: /tmp/data.flat",
    "# confidence: 0.950",
    "       par             est             low            high             lbl",
    "      T_xy      0.50000000      0.46250000      0.53750000       fit_main",
    "     T_xyz      2.00000000      1.90625000      2.09375000       fit_main"
  )
  tmp <- tempfile(fileext = ".bootci")
  on.exit(unlink(tmp))
  writeLines(lbl_content, tmp)
  result <- read_legofit_bootstrap(tmp)
  expect_true("lbl" %in% names(result))
  expect_equal(result$lbl, c("fit_main", "fit_main"))
})

test_that("T1.5: bootci at --confidence 0.99 parses cleanly", {
  c99_content <- c(
    "# bootci.py run at: 2026-05-21 10:00:00",
    "# input: /tmp/data.flat",
    "# confidence: 0.990",
    "       par             est             low            high",
    "      T_xy      0.50000000      0.40000000      0.60000000",
    "     T_xyz      2.00000000      1.80000000      2.20000000"
  )
  tmp <- tempfile(fileext = ".bootci")
  on.exit(unlink(tmp))
  writeLines(c99_content, tmp)
  result <- read_legofit_bootstrap(tmp)
  expect_equal(nrow(result), 2L)
  expect_false("lbl" %in% names(result))
  # Wider bounds for 0.99 confidence
  xy <- result[result$parameter == "T_xy", ]
  expect_equal(xy$hi - xy$lo, 0.2, tolerance = 1e-6)
})

test_that("T1.5: bootci with both -l and --confidence 0.99 parses correctly", {
  both_content <- c(
